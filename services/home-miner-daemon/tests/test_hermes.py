class TestHermesEventFilter:
    """Tests for hermes.get_filtered_events() function."""

    def test_hermes_event_filter(self):
        """user_message events are filtered out."""
        token = hermes.create_authority_token("hermes-020", ["observe"], 24)
        connection = connect(token)
        
        # Create some events including user_message
        principal_id = connection.principal_id
        spine.append_event(EventKind.HERMES_SUMMARY, principal_id, {"test": "hermes_summary"})
        spine.append_event(EventKind.MINER_ALERT, principal_id, {"test": "miner_alert"})
        spine.append_event(EventKind.USER_MESSAGE, principal_id, {"test": "user_message"})
        spine.append_event(EventKind.CONTROL_RECEIPT, principal_id, {"test": "control_receipt"})
        
        events = get_filtered_events(connection, limit=10)
        
        # Verify no user_message events
        event_kinds = [e["kind"] for e in events]
        assert "user_message" not in event_kinds
        assert "hermes_summary" in event_kinds
        assert "miner_alert" in event_kinds
        assert "control_receipt" in event_kinds

    def test_hermes_summary_appears_in_inbox(self):
        """Appended summary is visible via filtered events."""
        token = hermes.create_authority_token("hermes-021", ["observe", "summarize"], 24)
        connection = connect(token)
        
        # Append a summary
        result = append_summary(connection, "Miner health check passed", "observe")
        
        # Read filtered events
        events = get_filtered_events(connection, limit=100)  # Higher limit to find it
        
        # Find the summary we just appended by event_id
        summary_event = next((e for e in events if e["id"] == result["event_id"]), None)
        assert summary_event is not None
        assert summary_event["payload"]["summary_text"] == "Miner health check passed"


class TestHermesPairing:
    """Tests for hermes.pair_hermes() function."""

    def test_hermes_pair_creates_record(self):
        """Pairing creates a Hermes pairing record."""
        pairing = pair_hermes("hermes-pair-001", "test-hermes")
        
        assert pairing.hermes_id == "hermes-pair-001"
        assert pairing.device_name == "test-hermes"
        assert "observe" in pairing.capabilities
        assert "summarize" in pairing.capabilities
        assert pairing.token is not None

    def test_hermes_pair_idempotent(self):
        """Pairing is idempotent - same hermes_id returns existing."""
        pair_hermes("hermes-pair-002", "test-hermes-2")
        pairing2 = pair_hermes("hermes-pair-002", "test-hermes-2-different")
        
        # Should return same pairing, ignore new device_name
        assert pairing2.hermes_id == "hermes-pair-002"


