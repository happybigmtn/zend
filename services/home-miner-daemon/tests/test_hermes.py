class TestHermesConnect:
    """Tests for hermes.connect() function."""

    def test_hermes_connect_valid(self):
        """Connect with valid token succeeds."""
        # Create a valid token
        token = hermes.create_authority_token("hermes-001", ["observe", "summarize"], 24)
        
        connection = connect(token)
        
        assert connection.hermes_id == "hermes-001"
        assert connection.principal_id is not None  # Generated by store
        assert "observe" in connection.capabilities
        assert "summarize" in connection.capabilities
        assert connection.authority_scope == "observe+summarize"

    def test_hermes_connect_expired(self):
        """Connect with expired token fails."""
        import base64
        token_data = {
            "hermes_id": "hermes-001",
            "principal_id": "principal-001",
            "capabilities": ["observe"],
            "expires_at": (datetime.now(timezone.utc) - timedelta(hours=1)).isoformat()
        }
        token = base64.b64encode(json.dumps(token_data).encode()).decode()
        
        with pytest.raises(HermesInvalidTokenError) as exc_info:
            connect(token)
        
        assert "expired" in str(exc_info.value).lower()

    def test_hermes_connect_malformed_token(self):
        """Reject tokens that aren't valid Base64 JSON."""
        with pytest.raises(HermesInvalidTokenError):
            connect("not-valid-base64!!!")

    def test_hermes_connect_missing_fields(self):
        """Reject tokens missing required fields."""
        import base64
        # Missing capabilities
        token_data = {
            "hermes_id": "hermes-001",
            "principal_id": "principal-001",
            "expires_at": (datetime.now(timezone.utc) + timedelta(hours=24)).isoformat()
        }
        token = base64.b64encode(json.dumps(token_data).encode()).decode()
        
        with pytest.raises(HermesInvalidTokenError) as exc_info:
            connect(token)
        
        assert "capabilities" in str(exc_info.value)

    def test_hermes_invalid_capability(self):
        """Reject tokens with invalid Hermes capabilities."""
        import base64
        token_data = {
            "hermes_id": "hermes-001",
            "principal_id": "principal-001",
            "capabilities": ["observe", "control"],  # control not allowed for Hermes
            "expires_at": (datetime.now(timezone.utc) + timedelta(hours=24)).isoformat()
        }
        token = base64.b64encode(json.dumps(token_data).encode()).decode()
        
        with pytest.raises(HermesInvalidTokenError) as exc_info:
            connect(token)
        
        assert "control" in str(exc_info.value).lower()

    def test_hermes_read_status(self):
        """Observe capability allows status read (no exception)."""
        token = hermes.create_authority_token("hermes-002", ["observe"], 24)
        connection = connect(token)
        
        # Test that read_status can be called with observe capability
        # We can't test the actual return value without running daemon,
        # but we can verify it doesn't raise HermesUnauthorizedError
        try:
            # This will fail because daemon.miner isn't running, but it should
            # not raise HermesUnauthorizedError (which would mean capability check failed)
            status = read_status(connection)
        except HermesUnauthorizedError:
            pytest.fail("read_status raised HermesUnauthorizedError for observe-capable connection")
        except Exception:
            # Expected - daemon.miner isn't available in test
            pass  # This is expected in unit tests without daemon
        
        # Verify capability is present
        assert "observe" in connection.capabilities

    def test_hermes_no_observe_capability(self):
        """Read status fails without observe capability."""
        import base64
        token_data = {
            "hermes_id": "hermes-003",
            "principal_id": "principal-001",
            "capabilities": [],  # No observe
            "expires_at": (datetime.now(timezone.utc) + timedelta(hours=24)).isoformat()
        }
        token = base64.b64encode(json.dumps(token_data).encode()).decode()
        connection = connect(token)
        
        with pytest.raises(HermesUnauthorizedError) as exc_info:
            read_status(connection)
        
        assert "observe" in str(exc_info.value).lower()


class TestHermesSummary:
    """Tests for hermes.append_summary() function."""

    def test_hermes_append_summary(self):
        """Summarize capability appends to spine."""
        token = hermes.create_authority_token("hermes-010", ["summarize"], 24)
        connection = connect(token)
        
        result = append_summary(
            connection,
            "Miner running normally at 50kH/s",
            "observe"
        )
        
        assert result["appended"] is True
        assert "event_id" in result
        
        # Verify event in spine
        events = spine.get_events(kind=EventKind.HERMES_SUMMARY, limit=1)
        assert len(events) > 0
        assert events[0].payload["summary_text"] == "Miner running normally at 50kH/s"
        assert events[0].payload["hermes_id"] == "hermes-010"

    def test_hermes_no_summarize_capability(self):
        """Append summary fails without summarize capability."""
        import base64
        token_data = {
            "hermes_id": "hermes-011",
            "principal_id": "principal-001",
            "capabilities": ["observe"],  # No summarize
            "expires_at": (datetime.now(timezone.utc) + timedelta(hours=24)).isoformat()
        }
        token = base64.b64encode(json.dumps(token_data).encode()).decode()
        connection = connect(token)
        
        with pytest.raises(HermesUnauthorizedError) as exc_info:
            append_summary(connection, "Test summary", "observe")
        
        assert "summarize" in str(exc_info.value).lower()

    def test_hermes_empty_summary_rejected(self):
        """Empty summary text is rejected."""
        token = hermes.create_authority_token("hermes-012", ["summarize"], 24)
